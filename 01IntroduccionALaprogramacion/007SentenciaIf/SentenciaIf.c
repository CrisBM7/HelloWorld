#include <stdio.h>

int main() {
    int a = 5;
    int b = 10;

    // Ejemplo con igualdad
    if (a == b) {
        printf("%d es Igual a %d\n",a ,b);
    } 
    
    // Ejemplo con desigualdad
    if (a != b) {
        printf("%d es Desigual a %d\n",a ,b);
    } 
    
    // Ejemplo con mayor que
    if (a > b) {
        printf("%d es Mayor que %d\n",a ,b);
    } 
    
    // Ejemplo con menor que
    if (a < b) {
        printf("%d es Menor que %d\n",a ,b);
    } 
    
    // Ejemplo con mayor o igual que
    if (a >= b) {
        printf("%d es Mayor o igual que %d\n",a ,b);
    } 
    
    // Ejemplo con menor o igual que
    if (a <= b) {
        printf("%d es Menor o igual que %d\n",a ,b);
    } 

}