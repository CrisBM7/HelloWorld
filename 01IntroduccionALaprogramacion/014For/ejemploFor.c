#include <stdio.h>

int main() {
    int i;

    // Inicialización: i se inicializa en 1
    // Condición: el bucle continuará mientras i sea menor o igual a 5
    // Incremento: después de cada iteración, i se incrementa en 1
    for (i = 1; i <= 5; i++) {
        printf("%d\n", i); // Imprime el valor actual de i
    }

    return 0;
}
