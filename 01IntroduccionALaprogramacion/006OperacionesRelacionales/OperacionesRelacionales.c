#include <stdio.h>

int main() {
    int a = 5;
    int b = 10;

    // Ejemplo con igualdad
    printf("Igualdad: %d\n", a == b);

    // Ejemplo con desigualdad
    printf("Desigualdad: %d\n", a != b);

    // Ejemplo con mayor que
    printf("Mayor que: %d\n", a > b);

    // Ejemplo con menor que
    printf("Menor que: %d\n", a < b);

    // Ejemplo con mayor o igual que
    printf("Mayor o igual que: %d\n", a >= b);

    // Ejemplo con menor o igual que
    printf("Menor o igual que: %d\n", a <= b);

    return 0;
}
